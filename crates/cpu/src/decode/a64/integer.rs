#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub struct Operands {
    pub rd: u8,
    pub rn: u8,
    pub ra: u8,
    pub rm: u8,
    pub nzcv: u8,
    pub condition: u8,
    pub shift_amount: u8,
    pub small_shift: u8,
    pub shift_kind: u8,
    pub immediate_6_high: u8,
    pub immediate_12: u16,
    pub immediate_16: u16,
    pub extension: u8,
    pub opcode_2: u8,
    pub opcode_3: u8,
    pub n: bool,
    pub bit10: bool,
    pub invert: bool,
    pub immediate_form: bool,
    pub subtract_product: bool,
    pub set_flags: bool,
    pub subtract: bool,
    pub width_64: bool,
    pub adr_immediate: u32,
}

macro_rules! instructions {
    ($($variant:ident),+ $(,)?) => {
        #[derive(Clone, Copy, Debug, Eq, PartialEq)]
        pub enum Instruction { $($variant(Operands)),+ }

        impl Instruction {
            #[must_use]
            pub const fn operands(self) -> Operands {
                match self { $(Self::$variant(value) => value,)+ }
            }
        }
    };
}

