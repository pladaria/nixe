pub(super) fn normalize(semantic_id: u32, bits: u32) -> Instruction {
    let operands = Operands {
        rd: (bits & 0x1f) as u8,
        rn: ((bits >> 5) & 0x1f) as u8,
        ra: ((bits >> 10) & 0x1f) as u8,
        rm: ((bits >> 16) & 0x1f) as u8,
        nzcv: (bits & 0xf) as u8,
        condition: ((bits >> 12) & 0xf) as u8,
        shift_amount: ((bits >> 10) & 0x3f) as u8,
        small_shift: ((bits >> 10) & 7) as u8,
        shift_kind: ((bits >> 22) & 3) as u8,
        immediate_6_high: ((bits >> 16) & 0x3f) as u8,
        immediate_12: ((bits >> 10) & 0xfff) as u16,
        immediate_16: ((bits >> 5) & 0xffff) as u16,
        extension: ((bits >> 13) & 7) as u8,
        opcode_2: ((bits >> 21) & 3) as u8,
        opcode_3: ((bits >> 21) & 7) as u8,
        n: bits & (1 << 22) != 0,
        bit10: bits & (1 << 10) != 0,
        invert: bits & (1 << 21) != 0,
        immediate_form: bits & (1 << 11) != 0,
        subtract_product: bits & (1 << 15) != 0,
        set_flags: bits & (1 << 29) != 0,
        subtract: bits & (1 << 30) != 0,
        width_64: bits & (1 << 31) != 0,
        adr_immediate: ((bits >> 3) & 0x1f_fffc) | ((bits >> 29) & 3),
    };
    match semantic_id {
        0x0000_0010 => Instruction::MoveWide(operands),
        0x0000_0003 => Instruction::AddSubImmediate(operands),
        0x0000_0011 => Instruction::AddSubShifted(operands),
        0x0000_0012 => Instruction::AddSubExtended(operands),
        0x0000_0013 => Instruction::AddSubCarry(operands),
        0x0000_0014 => Instruction::LogicalImmediate(operands),
        0x0000_0015 => Instruction::LogicalShifted(operands),
        0x0000_0016 => Instruction::Bitfield(operands),
        0x0000_0017 => Instruction::Extract(operands),
        0x0000_0018 => Instruction::TwoSource(operands),
        0x0000_0019 => Instruction::ConditionalCompareRegister(operands),
        0x0000_001a => Instruction::ConditionalCompareImmediate(operands),
        0x0000_001b => Instruction::ConditionalSelect(operands),
        0x0000_001c => Instruction::ThreeSource(operands),
        0x0000_001d => Instruction::OneSource(operands),
        0x0000_0020 => Instruction::Adr(operands),
        0x0000_0021 => Instruction::Adrp(operands),
        _ => unreachable!("integer semantic ID was routed to the wrong family"),
    }
}
